def main():
    dataset = [{"prompt": p, "label": "good", "hash": h(p)} for p in GOOD[:70]]
    dataset += [{"prompt": p, "label": "bad", "hash": h(p)} for p in BAD[:70]]
    dataset += [{"prompt": p, "label": "borderline", "hash": h(p)} for p in BORDERLINE[:60]]
    random.Random(42).shuffle(dataset)
    json.dump(dataset, open(OUT, "w"), indent=2, ensure_ascii=False)
    from collections import Counter
    c = Counter(d["label"] for d in dataset)
    print(f"Generated {len(dataset)}: good={c['good']} bad={c['bad']} borderline={c['borderline']}")

if __name__ == "__main__":
    main()
